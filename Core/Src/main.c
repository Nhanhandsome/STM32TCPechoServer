static void tcp_client_thread(void *arg) {
	int buflen = 150;
	int ret;
	unsigned char recv_buffer[150];
	sock_client *socket;
	socket = (sock_client*) arg;
	int conn;
	socklen_t size;
	struct sockaddr_in remotehost;
	conn = socket->conn;
	size = socket->size;
	remotehost = socket->remotehost;
	timeout = tick;
	/* Read in the request */
	for (;;) {

		if (tick - timeout > 20) {
			close(conn);
			osThreadExit();
		} //tick = osKernelGetTickCount();
		ret = recvfrom(conn, (char* ) recv_buffer, buflen, 0,
				(struct sockaddr* )&remotehost, (socklen_t* )&size);
		if (ret > 0) {
			timeout = tick;
		}
		if (ret <= 0) {
			close(conn);
			osThreadExit();
		}
		sendto(conn, (char* )recv_buffer, ret, 0,
				(struct sockaddr* )&remotehost, (socklen_t )size);
		if (strstr((char*) recv_buffer, "-c") != NULL) {
			close(conn);
			osThreadExit();
			//return;
		}
		/* Check if request to get ST.gif */
	}
	/* Close connection socket */
//	close(conn);
}
void http_server_serve(sock_client socket) {
	int buflen = 1500;
	int ret;
	unsigned char recv_buffer[1500];
	int conn, size;
	struct sockaddr_in remotehost;
	conn = socket.conn;
	size = socket.size;
	remotehost = socket.remotehost;
	/* Read in the request */
	ret = recvfrom(conn, recv_buffer, buflen, 0, (struct sockaddr* )&remotehost,
			(socklen_t* )&size);
	if (ret < 0)
		return;
	sendto(conn, (char* )recv_buffer, ret, 0, (struct sockaddr* )&remotehost,
			(socklen_t )size);
	/* Check if request to get ST.gif */
	/* Close connection socket */
	close(conn);
}
void tcp_server_init(void) {
	sock_port.port = 80;

	sys_thread_new("HTTP", tcp_server_thread, (void*) &sock_port,
	DEFAULT_THREAD_STACKSIZE * 4, osPriorityBelowNormal);
}
static void tcp_server_thread(void *arg) {
	int sock, newconn;
	socklen_t size;
	struct sockaddr_in address, remotehost;
	socket_port *sport;
	sport = (socket_port*) arg;
	/* create a TCP socket */
	if ((sock = socket(AF_INET, SOCK_STREAM, 0)) < 0) {
		return;
	}

	/* bind to port 80 at any interface */
	address.sin_family = AF_INET;
	address.sin_port = htons(sport->port);
	address.sin_addr.s_addr = INADDR_ANY;

	if (bind(sock, (struct sockaddr *)&address, sizeof (address)) < 0) {
		return;
	}

	/* listen for incoming connections (TCP listen backlog = 5) */
	listen(sock, 10);
	//connect()
	//size = sizeof(remotehost);

	while (1) {
		newconn = accept(sock, (struct sockaddr* )&remotehost,
				(socklen_t* )&size);
		if (newconn >= 0) {
			socketClient.conn = newconn;
			socketClient.size = size;
			socketClient.remotehost = remotehost;
			sys_thread_new("tcp_client_thread", tcp_client_thread,
					(void*) &socketClient, DEFAULT_THREAD_STACKSIZE * 4,
					osPriorityNormal);
			//tcp_server_init();
			// http_server_serve(socketClient);
		}
		osDelay(1);
	}
}
